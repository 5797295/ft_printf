#include "libft.h"

int		ft_match(char *s1, char *s2)
{
	while (*s2)
	{
		if (*s2 == '#')
		{
			if (ft_match(s1, s2 + 1))
				return (1);
			if (ft_match(s1 + 1, s2))
				return (1);
			return (0);
		}
		else
		{
			if (*(s1++) != *(s2++))
				return (0);
		}
	}
	return (*s1 == '\0' && *s2 == '\0');
}
